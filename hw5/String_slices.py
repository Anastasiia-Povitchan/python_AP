a = input("Введите строку:")
print("Третий символ строки: ", a[2])
print("Предпоследний символ строки: ", a[-2])
print("Первые 5 символов: ", a[:5])
print("Вся строка, кроме поледних 2х символов: ", a[0:-2])
print("Символы с четными индексами: ", a[::2])
print("Символы с нечетными индексами: ", a[1::2])
print("В обратном порядке: ", a[::-1])
print("Символы строки через один в обратном порядке, начиная с последнего: ", a[::-2])
print("Длина строки: ", len(a))
